// Antiga forma de declarar variaveis no JS
var texte1 = 'ola mundo'
console.log(texte1)

// let - Os valores das declarações podem ser substituídos
let texte2 = 'Olá mundo!'
console.log(texte2)

let fruta
fruta = 'Banana'
console.log(fruta)

fruta = 'maçã'
console.log(fruta)

let falso = fruta == 'Maçã'
console.log(falso)

let verdade = fruta == 'maçã'
console.log(verdade)

// const -  Os valores das declarações não podem ser substiuídos e a variável deve ser iniciada com um valor
const texte3 = 'Olá, mundo!'
console.log(texte3)

const carro = 'gol'
console.log(carro)

